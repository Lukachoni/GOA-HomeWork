
ტემპერატურა = [72, 68, 75, 70, 78, 74, 71]


უმაღლესი_ტემპერატურა = max(ტემპერატურა)
print("უმაღლესი ტემპერატურა:", უმაღლესი_ტემპერატურა)


ყველაზე_დაბალი_ტემპერატურა = min(ტემპერატურა)
print("ყველაზე დაბალი ტემპერატურა:", ყველაზე_დაბალი_ტემპერატურა)


საშუალო_ტემპერატურა = sum(ტემპერატურა) / len(ტემპერატურა)
print("საშუალო ტემპერატურა:", საშუალო_ტემპერატურა)


above_70 = [temp for temp in ტემპერატურა if temp > 70]


print("ზემოთ 70 გრადუსზე:", above_70)